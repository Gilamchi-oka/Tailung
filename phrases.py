LEVEL_NAMES = [
    (0,    "🥚 Яйцо"),
    (200,  "🐣 Птенец"),
    (500,  "🐦 Боец"),
    (1000, "🦅 Охотник"),
    (2000, "🐯 Тигр"),
    (3500, "🐉 Дракон"),
    (5000, "⚡ Тайлунг"),
]

def get_level(xp: int) -> str:
    level = LEVEL_NAMES[0][1]
    for threshold, name in LEVEL_NAMES:
        if xp >= threshold:
            level = name
    return level

def morning_text(name: str, run_km: float, streak: int, xp: int) -> str:
    intro = random.choice(MORNING_INTROS).format(name=name)
    level = get_level(xp)
    streak_line = f"🔥 Стрик: {streak} дней" if streak > 0 else "⚪ Стрик: 0 (начни сегодня)"
    return (
        f"{intro}\n\n"
        f"{streak_line} | {level} | ⚡ {xp} XP\n\n"
        f"📋 *Задачи на сегодня:*\n"
        f"🏃 Бег — {run_km:.1f} км\n"
        f"📖 Чтение — 30 страниц\n"
        f"💻 Работа над проектом — 2 часа\n"
        f"🥗 Питание — без мусора\n"
        f"🧘 Медитация — 10 мин\n\n"
        f"Вечером жду отчёт. Без отмазок."
    )

def evening_text(name: str) -> str:
    return random.choice(EVENING_INTROS).format(name=name)

def get_random_challenge() -> str:
    return random.choice(CHALLENGE_LIST)

def streak_message(streak: int) -> str | None:
    return STREAK_MSGS.get(streak)

def fail_message(streak: int) -> str:
    msg = random.choice(FAIL_MSGS)
    return msg.format(streak=streak)